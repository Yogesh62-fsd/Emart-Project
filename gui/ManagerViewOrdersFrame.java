    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        try {
              tm = (DefaultTableModel) jtOrdersdetails.getModel();
              tm.setRowCount(0);
            String orderid = jcOrders.getSelectedItem().toString();
            Map<String, Long> pidlist = OrderDAO.getAllProductByOrderId(orderid);
            for (Map.Entry<String, Long> e : pidlist.entrySet()) {
                String pid = e.getKey();
                Long quant = e.getValue();
                ProductPojo p = ProductDAO.getProductsById(pid);
                Object[] rows = new Object[8];
                rows[0] = p.getProductId();
                rows[1] = p.getProductName();
                rows[2] = p.getProductCompany();
                rows[3] = p.getProductPrice();
                rows[4] = p.getOurPrice();
                rows[5] = quant;
                rows[6] = p.getTax();
                total=quant * p.getOurPrice() +(p.getOurPrice()*p.getTax() / 100);
                rows[7] = (total);
                tm.addRow(rows);
              
                 jtOrdersdetails.removeAll();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Db error", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
      
       
    }//GEN-LAST:event_btnShowActionPerformed

