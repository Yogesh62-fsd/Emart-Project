    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        int result = validateInput();
        if (result == 0) {
            JOptionPane.showMessageDialog(null, "Passord must be atleasr 4 character", "Invalid ", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (result == -1) {
            JOptionPane.showMessageDialog(null, "Please enter user Id", "Invalid ", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String p1 = String.valueOf(txtPassword.getPassword());
        String p2 = String.valueOf(txtRePassword.getPassword());
        boolean check = isPasswordMatching(p1, p2);
        if (check == false) {
            JOptionPane.showMessageDialog(null, "Passord Do Not Match ", "Invalid ", JOptionPane.ERROR_MESSAGE);
            return;
        }
        UserPojo u = new UserPojo();
        u.setUserid(txtUserId.getText());
        u.setEmpid(jcRecpId.getSelectedItem().toString());
        u.setUsername(txtRecpName.getText());
        u.setPassword(String.valueOf(txtPassword.getPassword()));
        u.setUsertype("Receptionist");
        u.setUsername(txtRecpName.getText());
        try {
            boolean status = ReceptionistDAO.addReceptionist(u);
            if (status) {
                JOptionPane.showMessageDialog(null, "Receptionist  Added Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                clearText();
               loadReceptionistDetails();
                return;
            }
            JOptionPane.showMessageDialog(null, "Receptionist Not Added ", "Failed", JOptionPane.ERROR_MESSAGE);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Db error", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnAddActionPerformed

