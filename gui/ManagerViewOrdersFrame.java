    private void loadAllOrderId() {
        try {
            List<String> orderid = OrderDAO.getAllOrderId();
            System.out.println("size of array is" + jcOrders.getItemCount());

            for (String s : orderid) {

                jcOrders.addItem(s);
                if (orderid.contains(s)) {
                    orderid.remove(true);
                }

                jcOrders.setMaximumRowCount(3);

            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DbERror", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }
