    private void allEmpId() {
       
        try{
             jcEmpId.removeAllItems();
             ArrayList<String>empid=EmployeeDAO.getAllEmpId();
             for(String id:empid)
             {
                 jcEmpId.addItem(id);
                 jcEmpId.setMaximumRowCount(4);
             }
        }catch(SQLException ex)
                 {
                   ex.printStackTrace();
                 }
        }
      private boolean validateInputs() {
      return!(txtEmpName.getText().isEmpty()||txtEmpSal.getText().isEmpty());
    }

    private void clearText() {
           jcEmpId.removeAllItems();
           txtEmpName.setText("  ");
           txtEmpSal.setText("  ");
        
    }

