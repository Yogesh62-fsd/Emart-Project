    public void loadReceptionistDetails() {
        try {

            receptionist = ReceptionistDAO.getNotRegisteredReceptionist();
            if (receptionist.isEmpty() == true) {
                JOptionPane.showMessageDialog(null, "No Not Registered Receptionist present");
                btnAdd.setEnabled(false);
                return;
            }
            btnAdd.setEnabled(true);
            Set<String> s = receptionist.keySet();
            Iterator<String> it = s.iterator();
            jcRecpId.removeAllItems();
            while (it.hasNext()) {
                jcRecpId.addItem(it.next());
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Db error", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

