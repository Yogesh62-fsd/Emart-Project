    private void allReceptionistId() {

        try {
            ArrayList<String> recpUserIdList = ReceptionistDAO.getAllUserId();
            if(recpUserIdList.isEmpty())
            {
                JOptionPane.showConfirmDialog(null, "No record of Receptionist");
                return;
            }
            btnRemove.setEnabled(true);
            for (String recpUserId : recpUserIdList) {
                jcUserId.addItem(recpUserId);

                System.out.println("id are" + recpUserId);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Db error", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
