    private void jcEmpIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcEmpIdActionPerformed
         if(jcEmpId.getItemCount()==0)
         {
             allEmpId();
         }
        String empid=jcEmpId.getSelectedItem().toString();
        try{
            EmployeePojo e=EmployeeDAO.findEmployeeById(empid);
             txtEmpName.setText(e.getEmpname());
             jcJob.setSelectedItem(e.getJob());
             txtEmpSal.setText(String.valueOf(e.getSal()));
        }catch(SQLException ex)
                 {
                   ex.printStackTrace();
                 }
    }//GEN-LAST:event_jcEmpIdActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
         try{
          if(!validateInputs())
          {
              JOptionPane.showMessageDialog(null, "Please fill the ufilled  details to Remove! ","INcompleteData",JOptionPane.INFORMATION_MESSAGE);
              return;
          }
        String empId=jcEmpId.getSelectedItem().toString();
        boolean result =EmployeeDAO.deleteEmployeeBYId(empId);
        if(!result)
        {
             JOptionPane.showMessageDialog(null, "Employee Details Not Removed", "failed", JOptionPane.INFORMATION_MESSAGE);
             return;
        }
        else{
        JOptionPane.showMessageDialog(null, "Employee details removed Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
     
           clearText();
                 
            System.out.println("jcEmpid"+jcEmpId.getSelectedItem()+" "+jcEmpId.getSelectedIndex());
            System.out.println("jcJob"+jcJob.getSelectedItem()+" "+jcJob.getSelectedIndex());
        }
        }
      catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Db error", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        } catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Please Inputs Sal in digits Only", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
       }
         catch(ArrayIndexOutOfBoundsException ed)
         {
             ed.printStackTrace();
         }
    }//GEN-LAST:event_btnRemoveActionPerformed

