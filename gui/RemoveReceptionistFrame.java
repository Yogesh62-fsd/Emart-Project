    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        String userid = jcUserId.getSelectedItem().toString();
   
        try {
            boolean result = ReceptionistDAO.removeAllUserId(userid);
            
            if (!result) {
                JOptionPane.showMessageDialog(null, "Sorry!Could Not Remove the UserIs", "Failed", JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(null, "UserId Successfully Removed", "Success", JOptionPane.INFORMATION_MESSAGE);
            jcUserId.removeAllItems();
            allReceptionistId();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Db error", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

