    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        if ( !validateInputs()) {
            JOptionPane.showMessageDialog(null, "Please inputs all field", "Information DATA", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String usertype = getUserType();
        if (usertype == null) {
            JOptionPane.showMessageDialog(null, "Please select usertype", "Information DATA", JOptionPane.ERROR_MESSAGE);
            return;
        }  
        UserPojo u = new UserPojo();
        u.setUserid(userid);
        u.setPassword(password);
        u.setUsertype(usertype);

        try {
            boolean result = UserDAO.validateUser(u);
            if (!result ) {
                JOptionPane.showMessageDialog(null,"Invalid userid/password/usertype !", "Error", JOptionPane.ERROR_MESSAGE);   
                return;  
            }
            UserProfile.setUserid(userid);
            UserProfile.setUsertype(usertype);
           
            if (usertype.equalsIgnoreCase("Manager")) 
               { 
                ManagerOptionsFrame mframe = new ManagerOptionsFrame();
                mframe.setVisible(true);
                this.dispose();
               }
            else {
                 ReceptionistOptionsFrame rframe = new ReceptionistOptionsFrame();
                 rframe.setVisible(true);
                 this.dispose();
              }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Db error", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

